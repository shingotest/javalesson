package co.jp.higschool;

public class EmpEntity {
	private String enpNo;
	private String Name;
	private String enpName;
	private String enpcode;

	public String getEnpNo() {
		return enpNo;
	}

	public void setEnpNo(String enpNo) {
		this.enpNo = enpNo;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public String getEnpName() {
		return enpName;
	}

	public void setEnpName(String enpName) {
		this.enpName = enpName;
	}

	public String getEnpcode() {
		return enpcode;
	}

	public void setEnpcode(String enpcode) {
		this.enpcode = enpcode;
	}
}
